function checkInput() {
	var inputField = document.getElementById("myInput");
	var inputValue = inputField.value;
  
	if (inputValue === "") {
	  alert("Input field is empty!");
	} else {
	  window.location.href = "https://www.netflix.com/signup/registration?locale=en-NG"; 
	}
}
  

